#!/bin/bash

# Скрипт для автоматического обновления системы и очистки пакетов

echo "Начинаем процесс обновления системы..."
echo "-----------------------------------"

# Обновление списка пакетов
sudo apt update

# Обновление всех установленных пакетов
sudo apt upgrade -y

# Обновление дистрибутива (если есть новые версии)
sudo apt dist-upgrade -y

# Удаление ненужных пакетов
sudo apt autoremove -y

# Очистка кеша пакетов
sudo apt clean

echo "-----------------------------------"
echo "Обновление системы завершено!"
