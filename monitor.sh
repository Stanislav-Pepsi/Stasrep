#!/bin/bash

# Скрипт для мониторинга основных ресурсов сервера

echo "Мониторинг ресурсов сервера"
echo "==========================="
echo "Дата и время: $(date)"
echo ""

# Информация о загрузке процессора
echo "Загрузка CPU:"
echo "-------------"
top -bn1 | grep "Cpu(s)"
echo ""

# Информация об использовании памяти
echo "Использование памяти:"
echo "---------------------"
free -h
echo ""

# Информация о дисковом пространстве
echo "Дисковое пространство:"
echo "----------------------"
df -h
echo ""

# Информация о работающих сервисах
echo "Работающие сервисы:"
echo "-------------------"
systemctl list-units --type=service --state=running | head -n 10
